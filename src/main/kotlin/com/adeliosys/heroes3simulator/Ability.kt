package com.adeliosys.heroes3simulator

enum class Ability {

    /**
     * Attack twice.
     */
    DOUBLE_ATTACK,

    /**
     * Prevent enemy retaliation.
     */
    NO_ENEMY_RETALIATION,

    /**
     * No melee penalty for a ranged creature.
     */
    NO_MELEE_PENALTY,

    /**
     * No distance penalty for a ranged creature.
     */
    NO_DISTANCE_PENALTY
}
